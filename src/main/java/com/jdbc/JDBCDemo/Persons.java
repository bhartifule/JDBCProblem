package com.jdbc.JDBCDemo;

public class Persons {
	int personID;
	String lastName;
	String firstName;
	int age;

	public Persons(int personID, String lastName, String firstName, int age) {
		super();
		this.personID = personID;
		this.lastName = lastName;
		this.firstName = firstName;
		this.age = age;
	}

}
